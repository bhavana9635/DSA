import java.util.*;

public class Bitwise {
    //Binary number system which is of 1's and 0's
    //Decimal ot binary and Binary to Decimal

    //Bitwise operator
    // and,or,xor,one's complemnt,left shift,right shift
    public static void main(String args[]){
        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(5 ^ 6);
        System.out.println(~5);
    }

}
